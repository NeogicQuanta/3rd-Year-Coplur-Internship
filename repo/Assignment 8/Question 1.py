import numpy as np

random_array = np.random.rand(4, 2)
empty_array = np.empty((4, 2))
full_array = np.full((4, 2), 7)
zeros_array = np.zeros((3, 5))
ones_array = np.ones((4, 3, 2))

print("Random array:\n", random_array)
print("\nEmpty array:\n", empty_array)
print("\nFull array:\n", full_array)
print("\nZeros array:\n", zeros_array)
print("\nOnes array:\n", ones_array)
